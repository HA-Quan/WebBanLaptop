package entity;

/**
 *
 * @author Ngon Roi Max Ngon
 */
public class Price {
    private int pid;
private String pname; 

    public Price() {
    }

    public Price(int pid, String pname) {
        this.pid = pid;
        this.pname = pname;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    @Override
    public String toString() {
        return "Price{" + "pid=" + pid + ", pname=" + pname + '}';
    }
    
}

